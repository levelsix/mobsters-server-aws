package com.lvl6.dynamo;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBVersionAttribute;

@DynamoDBTable(tableName="UserLockBoxItem")
public class UserLockBoxItem {



	private String id;
	private Long version;

	private int lockBoxItemId;
	private int userId;
	private int quantity;  
	public UserLockBoxItem(){}
  public UserLockBoxItem(int lockBoxItemId, int userId,
      int quantity) {
   
    this.lockBoxItemId = lockBoxItemId;
    this.userId = userId;
    this.quantity = quantity;
  }


	@DynamoDBHashKey(attributeName = "id")
	@DynamoDBAutoGeneratedKey
	public String getId(){return id;}
	public void setId(String id){this.id = id;}


	@DynamoDBVersionAttribute
	public Long getVersion(){return version;}
	public void setVersion(Long version){this.version = version;}


  public int getLockBoxItemId() {    return lockBoxItemId;
  }

  public void setLockBoxItemId(int lockBoxItemId) {
    this.lockBoxItemId = lockBoxItemId;
  }

  public int getUserId() {
    return userId;
  }

  public void setUserId(int userId) {
    this.userId = userId;
  }

  public int getQuantity() {
    return quantity;
  }

  public void setQuantity(int quantity) {
    this.quantity = quantity;
  }

  @Override
  public String toString() {
    return "UserLockBoxItem [lockBoxItemId=" + lockBoxItemId + ", userId="
        + userId + ", quantity=" + quantity + "]";
  }

 	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		UserLockBoxItem other = (UserLockBoxItem) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

}