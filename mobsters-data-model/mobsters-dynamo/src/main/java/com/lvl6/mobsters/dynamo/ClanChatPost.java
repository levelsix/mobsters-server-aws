package com.lvl6.mobsters.dynamo;
import java.util.Date;


import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBVersionAttribute;

@DynamoDBTable(tableName="ClanChatPost")
public class ClanChatPost {



	private String id;
	private Long version;
	
	private int posterId;
	private int clanId;
	private Date timeOfPost;
	private String content;
	public ClanChatPost(){}
  public ClanChatPost(int id, int posterId, int clanId,
      Date timeOfPost, String content) {
    super();
    this.posterId = posterId;
    this.clanId = clanId;
    this.timeOfPost = timeOfPost;
    this.content = content;
  }



	@DynamoDBHashKey(attributeName = "id")
	@DynamoDBAutoGeneratedKey
	public String getId(){return id;}
	public void setId(String id){this.id = id;}


	@DynamoDBVersionAttribute
	public Long getVersion(){return version;}
	public void setVersion(Long version){this.version = version;}


  public int getPosterId() {    return posterId;
  }

  public int getClanId() {
    return clanId;
  }

  public Date getTimeOfPost() {
    return timeOfPost;
  }

  public String getContent() {
    return content;
  }

  @Override
  public String toString() {
    return "ClanChatPost [id=" + id + ", posterId=" + posterId
        + ", clanID=" + clanId + ", timeOfPost=" + timeOfPost
        + ", content=" + content + "]";
  }
 	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ClanChatPost other = (ClanChatPost) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

}