package com.lvl6.mobsters.dynamo;
import java.util.Set;


import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBVersionAttribute;

@DynamoDBTable(tableName="Dialogue")
public class Dialogue {



	private String id;
	private Long version;

	
	
	
	Set<Boolean> isLeftSides;
	
	
	Set<String> speakers;
	
	Set<String> speakerImages;
	
	Set<String> speakerTexts;

	public Dialogue(){}
	public Dialogue(Set<String> speakers, Set<String> speakerImages, Set<String> speakerTexts, Set<Boolean> isLeftSides) {
		this.speakers = speakers;
		this.speakerTexts = speakerTexts;
		this.speakerImages = speakerImages;
		this.isLeftSides = isLeftSides;
	}

  public Set<Boolean> getIsLeftSides() {
    return isLeftSides;
  }

  public Set<String> getSpeakers() {
    return speakers;
  }

  public Set<String> getSpeakerImages() {
    return speakerImages;
  }

	public Set<String> getSpeakerTexts() {
		return speakerTexts;
	}

	@Override
	public String toString() {
		return "Dialogue [speakers=" + speakers + ", speakerTexts="
				+ speakerTexts + "]";
	}

 	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Dialogue other = (Dialogue) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

}